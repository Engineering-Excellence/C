#include <stdio.h>
#include <math.h>

int main(void) {
    printf("sin(π/2) = %f\n", sin(M_PI / 2));
    printf("cos(π/2) = %f\n", cos(M_PI / 2));
    printf("tan(π/2) = %f\n", tan(M_PI / 2));
    printf("ln(10) = %f\n", log(10));
    printf("log10(100) = %f\n", log10(100));
    printf("exp(10) = %f\n", exp(10));

    return 0;
}
